Citizen.CreateThread(function()
  while true do

    Citizen.Wait(900)

    if CurrentAction ~= nil then

      SetTextComponentFormat('STRING')
      AddTextComponentString(CurrentActionMsg)
      DisplayHelpTextFromStringLabel(0, 0, 1, -1)

      if IsControlPressed(0,  Keys['E']) and PlayerData.job ~= nil and PlayerData.job.name == 'harviis_hemp' and (GetGameTimer() - GUI.Time) > 150 then

        if CurrentAction == 'menu_armory' then
          OpenArmoryMenu(CurrentActionData.station)
        end

        if CurrentAction == 'menu_boss_actions' then

          ESX.UI.Menu.CloseAll()

          TriggerEvent('esx_society:openBossMenu', 'harviis_hemp', function(data, menu)

            menu.close()

            CurrentAction     = 'menu_boss_actions'
            CurrentActionMsg  = _U('open_bossmenu')
            CurrentActionData = {}

          end)

        end

        CurrentAction = nil
        GUI.Time      = GetGameTimer()

      end

    end

   if IsControlPressed(0,  Keys['F11']) and PlayerData.job ~= nil and PlayerData.job.name == 'harviis_hemp' and not ESX.UI.Menu.IsOpen('default', GetCurrentResourceName(), 'harviis_hemp_actions') and (GetGameTimer() - GUI.Time) > 150 then
